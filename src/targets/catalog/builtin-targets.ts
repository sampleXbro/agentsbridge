/**
 * Single source of truth for the descriptor-level conversion guard
 * (`supportsConversion`). When a target declares it can fall back to a
 * skill projection for commands/agents, we let the user disable that
 * projection via config; the result is observed identically by the
 * matrix display (effective support level) and the generate dispatch
 * (feature generator lookup), so both must read this helper.
 */
function isFeatureSuppressedByConversion(
  descriptor: TargetDescriptor | undefined,
  feature: keyof TargetCapabilities,
  config: ValidatedConfig | undefined,
  scope: TargetLayoutScope,
): boolean {
  if (!descriptor || !config) return false;
  if (feature === 'commands' && descriptor.supportsConversion?.commands) {
    return !shouldConvertCommandsToSkills(config, descriptor.id, true, scope);
  }
  if (feature === 'agents' && descriptor.supportsConversion?.agents) {
    return !shouldConvertAgentsToSkills(config, descriptor.id, true, scope);
  }
  return false;
}

function isConversionUpgrading(
  descriptor: TargetDescriptor | undefined,
  feature: keyof TargetCapabilities,
  config: ValidatedConfig | undefined,
  scope: TargetLayoutScope,
): boolean {
  if (!descriptor || !config) return false;
  if (feature === 'commands' && descriptor.supportsConversion?.commands) {
    return shouldConvertCommandsToSkills(config, descriptor.id, true, scope);
  }
  if (feature === 'agents' && descriptor.supportsConversion?.agents) {
    return shouldConvertAgentsToSkills(config, descriptor.id, true, scope);
  }
  return false;
}

export function getEffectiveTargetSupportLevel(
  target: string,
  feature: keyof TargetCapabilities,
  config: ValidatedConfig,
  scope: TargetLayoutScope = 'project',
): SupportLevel {
  const baseLevel = getTargetCapabilities(target, scope)?.[feature]?.level ?? 'none';
  const descriptor = getBuiltinTargetDefinition(target) ?? getDescriptor(target);
  if (baseLevel === 'none' && isConversionUpgrading(descriptor, feature, config, scope)) {
    return 'embedded';
  }
  if (baseLevel !== 'embedded') return baseLevel;
  return isFeatureSuppressedByConversion(descriptor, feature, config, scope) ? 'none' : baseLevel;
}

type FeatureGeneratorPick = (g: TargetGenerators) => FeatureGeneratorFn | undefined;

const PICK_FEATURE_GENERATOR: Record<TargetFeature, FeatureGeneratorPick | null> = {
  rules: (g) => g.generateRules,
  additionalRules: null,
  commands: (g) => g.generateCommands,
  agents: (g) => g.generateAgents,
  skills: (g) => g.generateSkills,
  mcp: (g) => g.generateMcp,
  permissions: (g) => g.generatePermissions,
  hooks: (g) => g.generateHooks,
  ignore: (g) => g.generateIgnore,
};

export function resolveTargetFeatureGenerator(
  target: string,
  feature: TargetFeature,
  config?: ValidatedConfig,
  scope: TargetLayoutScope = 'project',
): FeatureGeneratorFn | undefined {
  const descriptor = getBuiltinTargetDefinition(target) ?? getDescriptor(target);
  if (!descriptor?.generators) return undefined;
  if (isFeatureSuppressedByConversion(descriptor, feature, config, scope)) return undefined;
  const pick = PICK_FEATURE_GENERATOR[feature];
  return pick === null ? undefined : pick(descriptor.generators);
}
